--CONSTRAINTS AND DATA TYPE
CREATE TABLE STUDENTMARKS2(
--PRIMAY KEY is used for restrict blanks and duplicate values
	serialno SERIAL PRIMARY KEY,
	age INTEGER CHECK(age>=18),
	salary NUMERIC(10,2),
	name VARCHAR(50) ,
	subject VARCHAR(50),
	email VARCHAR(50) UNIQUE,
	registration_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	description TEXT,
	marks DOUBLE PRECISION
);

